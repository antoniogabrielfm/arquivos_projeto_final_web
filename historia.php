<h2>Nossa História</h2>
<p>Projetado em 1952 pelo arquiteto Carlos Alberto Holanda Mendonça segundo os princípios da arquitetura modernista, o Edifício Formac assumiu relevância no cenário arquitetônico da cidade desde a sua construção. Entre outros fatores, destacavam-se o porte da obra e a utilização de elementos inovadores na composição de suas fachadas, como brises e cobogós.</p>

<p>O Edifício Formac pela localização privilegiada, faz parte da paisagem turística, próximo de pontos importantes da cidade, como Cais do Porto e Rio Guaíba, sendo ponto de referência de quem chega a Porto Alegre e oferecendo uma vista privilegiada do Por do Sol.</p>

<p>Após sucessivas intervenções pontuais e particulares e com o desgaste natural dos materiais, as fachadas deterioraram-se parcialmente, necessitando de uma ação mais efetiva de manutenção. Essa iniciativa, além de atender às situações de segurança e higiene, resultará em uma natural valorização do imóvel. Paralelamente a isso, pela localização privilegiada, muito próxima do antigo porto, cenário da principal intervenção urbana futura da cidade, o Formac pode novamente ter um papel destacado nessa área.</p>

<p>Desde o ano de 2008, a administração do prédio tem investido maciçamente em uma reforma e modernização de seu hall de entrada, reprojetado com materiais da arquitetura moderna, sistema de controle de acesso, o mais moderno conjunto de elevadores e equipe treinada para atender da melhor forma o cliente.</p>

<p>As reformas se estendem a estrutura hidráulica dos 26 andares, a qual está sendo totalmente substituída por materiais mais duráveis do que os utilizados na época de sua construção, e também na implantação de medidas de prevenção a incêndio e constante treinamento de pessoal, contratação de bombeiro com plantão de 12 horas de segunda a sexta e técnico em manutenção especializado, tudo isso priorizando a segurança das mais de 3000 pessoas que acessam o prédio diariamente.</p>

